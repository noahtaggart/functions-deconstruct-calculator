const add = (num1, num2) => {
    const sum = num1 + num2
    return sum
}

let result = add(17, 9)
console.log(result)

const subtract = (num1, num2) => {
    const difference = num1 - num2
    return difference
}
result = subtract(90, 21)
console.log(result)

const divide = (num1, num2) => {
    const quotient = num1 / num2
    return quotient
}
result = divide(840, 2)
console.log(result)